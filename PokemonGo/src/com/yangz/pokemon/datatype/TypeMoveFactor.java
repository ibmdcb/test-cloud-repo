package com.yangz.pokemon.datatype;

public class TypeMoveFactor {
	String attackMove;
	String defenseType;
	int factor;
	public String getAttackMove() {
		return attackMove;
	}
	public void setAttackMove(String attackMove) {
		this.attackMove = attackMove;
	}
	public String getDefenseType() {
		return defenseType;
	}
	public void setDefenseType(String defenseType) {
		this.defenseType = defenseType;
	}
	public int getFactor() {
		return factor;
	}
	public void setFactor(int factor) {
		this.factor = factor;
	}

}
